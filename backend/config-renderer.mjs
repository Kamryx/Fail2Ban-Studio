function multilineValue(values) {
  if (!Array.isArray(values) || values.length === 0) {
    return "";
  }

  const [first, ...rest] = values;

  if (rest.length === 0) {
    return first;
  }

  return `${first}\n           ${rest.join("\n           ")}`;
}

function serializeKeyValue(key, value) {
  if (value == null) {
    return null;
  }

  if (Array.isArray(value)) {
    const filtered = value.map((entry) => `${entry}`.trim()).filter(Boolean);
    return filtered.length > 0 ? `${key} = ${multilineValue(filtered)}` : null;
  }

  if (typeof value === "boolean") {
    return `${key} = ${value ? "true" : "false"}`;
  }

  const text = `${value}`.trim();
  return text ? `${key} = ${text}` : null;
}

function serializeSection(title, rows) {
  const lines = rows
    .map(([key, value]) => serializeKeyValue(key, value))
    .filter(Boolean);

  if (lines.length === 0) {
    return "";
  }

  return [`[${title}]`, ...lines, ""].join("\n");
}

export function validateState(state) {
  const issues = [];
  const seen = new Set();

  for (const jail of state.jails ?? []) {
    const name = `${jail.name ?? ""}`.trim();

    if (!name) {
      issues.push("Every jail needs a name.");
      continue;
    }

    if (!/^[A-Za-z0-9._-]+$/.test(name)) {
      issues.push(`"${name}" contains unsupported characters. Use letters, numbers, dots, dashes, or underscores.`);
    }

    if (seen.has(name)) {
      issues.push(`"${name}" is listed more than once.`);
    }

    seen.add(name);

    if (jail.enabled && jail.source === "custom") {
      if (!`${jail.filter ?? ""}`.trim()) {
        issues.push(`Custom jail "${name}" needs a filter when it is enabled.`);
      }

      if (!`${jail.logpath ?? ""}`.trim()) {
        issues.push(`Custom jail "${name}" needs a log path when it is enabled.`);
      }
    }
  }

  return issues;
}

export function renderConfig(state) {
  const defaults = state.defaults ?? {};
  const header = [
    "# Fail2ban Studio managed file",
    "# This file is generated by the companion UI.",
    "# Recommended practice from the LinuxServer image is to keep custom overrides",
    "# in *.local files instead of editing shipped *.conf files directly.",
    ""
  ].join("\n");

  const defaultSection = serializeSection("DEFAULT", [
    ["ignoreip", defaults.ignoreip ?? []],
    ["bantime", defaults.bantime],
    ["findtime", defaults.findtime],
    ["maxretry", defaults.maxretry],
    ["chain", defaults.chain],
    ["action", defaults.action],
    ["bantime.increment", defaults.bantimeIncrement],
    ["bantime.maxtime", defaults.bantimeIncrement ? defaults.bantimeMaxtime : ""],
    ["bantime.factor", defaults.bantimeIncrement ? defaults.bantimeFactor : ""]
  ]);

  const jailSections = (state.jails ?? [])
    .map((jail) => {
      const name = `${jail.name ?? ""}`.trim();

      if (!name) {
        return "";
      }

      return serializeSection(name, [
        ["enabled", jail.enabled ?? false],
        ["chain", jail.chain],
        ["action", jail.action],
        ["port", jail.port],
        ["filter", jail.filter],
        ["logpath", jail.logpath],
        ["backend", jail.backend],
        ["banaction", jail.banaction],
        ["mode", jail.mode],
        ["maxretry", jail.maxretry],
        ["findtime", jail.findtime],
        ["bantime", jail.bantime]
      ]);
    })
    .filter(Boolean)
    .join("\n");

  return [header, defaultSection, jailSections].filter(Boolean).join("\n").trimEnd() + "\n";
}
