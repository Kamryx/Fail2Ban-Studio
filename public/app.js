function setPath(target, keyPath, value) {
  const parts = keyPath.split(".");
  let pointer = target;

  while (parts.length > 1) {
    const current = parts.shift();
    if (!(current in pointer)) {
      pointer[current] = {};
    }
    pointer = pointer[current];
  }

  pointer[parts[0]] = value;
}

function recommendedChain(target) {
  return target === "host" ? "INPUT" : "DOCKER-USER";
}

function renderValue(key, value) {
  if (value == null) {
    return null;
  }

  if (Array.isArray(value)) {
    const lines = value.map((entry) => `${entry}`.trim()).filter(Boolean);

    if (lines.length === 0) {
      return null;
    }

    if (lines.length === 1) {
      return `${key} = ${lines[0]}`;
    }

    return `${key} = ${lines[0]}\n           ${lines.slice(1).join("\n           ")}`;
  }

  if (typeof value === "boolean") {
    return `${key} = ${value ? "true" : "false"}`;
  }

  const text = `${value}`.trim();
  return text ? `${key} = ${text}` : null;
}

function renderSection(title, entries) {
  const lines = entries
    .map(([key, value]) => renderValue(key, value))
    .filter(Boolean);

  if (lines.length === 0) {
    return "";
  }

  return [`[${title}]`, ...lines, ""].join("\n");
}

function renderManagedConfig(state) {
  const header = [
    "# Fail2ban Studio managed file",
    "# This file is generated by the companion UI.",
    ""
  ].join("\n");

  const defaults = state.defaults;
  const defaultSection = renderSection("DEFAULT", [
    ["ignoreip", defaults.ignoreip],
    ["bantime", defaults.bantime],
    ["findtime", defaults.findtime],
    ["maxretry", defaults.maxretry],
    ["chain", defaults.chain],
    ["action", defaults.action],
    ["bantime.increment", defaults.bantimeIncrement],
    ["bantime.maxtime", defaults.bantimeIncrement ? defaults.bantimeMaxtime : ""],
    ["bantime.factor", defaults.bantimeIncrement ? defaults.bantimeFactor : ""]
  ]);
  const jailSections = state.jails
    .map((jail) =>
      renderSection(jail.name, [
        ["enabled", jail.enabled],
        ["chain", jail.chain],
        ["action", jail.action],
        ["port", jail.port],
        ["filter", jail.filter],
        ["logpath", jail.logpath],
        ["backend", jail.backend],
        ["banaction", jail.banaction],
        ["mode", jail.mode],
        ["maxretry", jail.maxretry],
        ["findtime", jail.findtime],
        ["bantime", jail.bantime]
      ])
    )
    .filter(Boolean)
    .join("\n");

  return `${[header, defaultSection, jailSections].filter(Boolean).join("\n").trimEnd()}\n`;
}

