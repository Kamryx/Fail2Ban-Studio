async function fetchJson(url, options = {}) {
  const response = await fetch(url, {
    headers: {
      "Content-Type": "application/json"
    },
    ...options
  });
  const payload = await response.json();

  if (!response.ok || payload.ok === false) {
    const issueList = payload.issues?.length ? ` ${payload.issues.join(" ")}` : "";
    throw new Error(`${payload.message || "Request failed."}${issueList}`);
  }

  return payload;
}

async function loadState({ preserveDraft = false } = {}) {
  if (!preserveDraft) {
    store.loading = true;
    render();
  }

  const snapshot = await fetchJson("/api/state");
  const previousDraft = preserveDraft ? store.draft : null;
  store.snapshot = snapshot;
  store.draft = previousDraft && isDirty() ? previousDraft : deepClone(snapshot.state);
  store.loading = false;
  store.refreshing = false;
  render();
}

async function loadLogs() {
  store.working = true;
  render();

  try {
    store.logs = await fetchJson("/api/logs?lines=160");
  } catch (error) {
    setNotice("error", error.message);
  } finally {
    store.working = false;
    render();
  }
}

function createJailTemplate(name, source, targetHint = "docker") {
  return {
    id: crypto.randomUUID(),
    name,
    source,
    enabled: false,
    target: targetHint,
    chain: recommendedChain(targetHint),
    action: "%(known/action)s",
    port: "",
    filter: "",
    logpath: "",
    backend: "",
    banaction: "",
    mode: "",
    maxretry: "",
    findtime: "",
    bantime: ""
  };
}

async function save(mode) {
  clearNotice();
  store.working = true;
  render();

  try {
    const endpoint = mode === "apply" ? "/api/apply" : "/api/save";
    const payload = await fetchJson(endpoint, {
      method: "POST",
      body: JSON.stringify({
        state: store.draft,
        reload: mode === "apply"
      })
    });

    await loadState({ preserveDraft: false });
    setNotice("success", payload.actionResult || "Configuration saved.");
    if (store.activeTab === "activity") {
      await loadLogs();
    }
  } catch (error) {
    setNotice("error", error.message);
  } finally {
    store.working = false;
    render();
  }
}

async function runAction(action, body) {
  clearNotice();
  store.working = true;
  render();

  try {
    const result = await fetchJson(`/api/actions/${action}`, {
      method: "POST",
      body: JSON.stringify(body)
    });
    await loadState({ preserveDraft: false });
    setNotice("success", result.message);
    if (store.activeTab === "activity") {
      await loadLogs();
    }
  } catch (error) {
    setNotice("error", error.message);
  } finally {
    store.working = false;
    render();
  }
}

app.addEventListener("click", async (event) => {
  const target = event.target.closest("[data-action], [data-tab]");

  if (!target) {
    return;
  }

  if (target.dataset.tab) {
    store.activeTab = target.dataset.tab;
    render();
    if (store.activeTab === "activity" && !store.logs) {
      await loadLogs();
    }
    return;
  }

  switch (target.dataset.action) {
    case "save":
      await save("save");
      break;
    case "apply":
      await save("apply");
      break;
    case "refresh":
      store.refreshing = true;
      render();
      await loadState({ preserveDraft: true });
      break;
    case "reload":
      await runAction("reload", { containerName: store.draft.setup.containerName });
      break;
    case "restart":
      await runAction("restart", { containerName: store.draft.setup.containerName });
      break;
    case "load-logs":
      await loadLogs();
      break;
    case "add-available-jail": {
      const picker = document.getElementById("available-jail-picker");
      const name = picker?.value;
      if (!name) {
        setNotice("error", "Choose a preset jail to add first.");
        return;
      }
      if (store.draft.jails.some((jail) => jail.name === name)) {
        setNotice("error", `The jail "${name}" is already in the list.`);
        return;
      }
      const choice = titleFromChoice(name, store.snapshot.availableJails);
      store.draft.jails.push(createJailTemplate(name, "preset", choice.targetHint || "docker"));
      render();
      break;
    }
    case "add-custom-jail":
      store.draft.jails.push(createJailTemplate(`custom-${store.draft.jails.length + 1}`, "custom", "docker"));
      render();
      break;
    case "remove-jail": {
      const jailId = target.dataset.jailId;
      store.draft.jails = store.draft.jails.filter((jail) => jail.id !== jailId);
      render();
      break;
    }
    case "unban":
      await runAction("unban", {
        containerName: store.draft.setup.containerName,
        jail: target.dataset.jail,
        ip: target.dataset.ip
      });
      break;
    default:
      break;
  }
});

function handleFieldUpdate(element) {
  if (!store.draft) {
    return;
  }

  if (element.dataset.bind) {
    let value;

    if (element.type === "checkbox") {
      value = element.checked;
    } else if (element.tagName === "TEXTAREA" && element.dataset.bind === "defaults.ignoreip") {
      value = splitLines(element.value);
    } else {
      value = element.value;
    }

    setPath(store.draft, element.dataset.bind, value);
    render();
    return;
  }

  const jailId = element.dataset.jailId;
  const jailField = element.dataset.jailField;

  if (!jailId || !jailField) {
    return;
  }

  const jail = store.draft.jails.find((entry) => entry.id === jailId);
  if (!jail) {
    return;
  }

  const previousTarget = jail.target;
  jail[jailField] = element.type === "checkbox" ? element.checked : element.value;

  if (jailField === "target" && jail.chain === recommendedChain(previousTarget)) {
    jail.chain = recommendedChain(jail.target);
  }

  render();
}

app.addEventListener("input", (event) => {
  const element = event.target;

  if (element.matches("[data-bind], [data-jail-id][data-jail-field]")) {
    handleFieldUpdate(element);
  }
});

app.addEventListener("change", (event) => {
  const element = event.target;

  if (element.matches("[data-bind], [data-jail-id][data-jail-field]")) {
    handleFieldUpdate(element);
  }
});

app.addEventListener("submit", async (event) => {
  if (event.target.id !== "manual-ban-form") {
    return;
  }

  event.preventDefault();
  const formData = new FormData(event.target);
  const jail = `${formData.get("jail") ?? ""}`.trim();
  const ip = `${formData.get("ip") ?? ""}`.trim();

  if (!jail || !ip) {
    setNotice("error", "Choose a jail and enter an IP address first.");
    return;
  }

  await runAction("ban", {
    containerName: store.draft.setup.containerName,
    jail,
    ip
  });
  event.target.reset();
});

async function boot() {
  try {
    await loadState();
  } catch (error) {
    store.loading = false;
    store.notice = { kind: "error", message: error.message };
    render();
  }
}

boot();
